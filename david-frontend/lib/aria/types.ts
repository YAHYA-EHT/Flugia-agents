export function autonomyDisplayName(actionType: string): string {
  switch (actionType) {
    case "reply_email":      return "Reply to emails";
    case "send_email":       return "Send new emails";
    case "draft_email":      return "Save email drafts";
    case "block_sender":     return "Block senders";
    case "mark_read":        return "Mark as read";
    case "archive_email":    return "Archive emails";
    case "create_event":     return "Create calendar events";
    case "delete_event":     return "Delete calendar events";
    case "schedule_reminder": return "Schedule reminders";
    default:                 return actionType;
  }
}

export function emailRuleActionLabel(action: string): string {
  switch (action) {
    case "block":
      return "Block";
    case "mark_read":
      return "Mark read";
    case "archive":
      return "Archive";
    case "notify_never":
      return "Never notify";
    default:
      return action;
  }
}

const truncate = (v: unknown, max = 60): string => {
  const s = String(v ?? "");
  return s.length <= max ? s : `${s.slice(0, max)}…`;
};

/** Human summary of a pending action, mirroring Flutter PendingAction.summary. */
export function pendingActionSummary(a: PendingAction): string {
  const p = a.payload ?? {};
  switch (a.actionType) {
    case "reply_email":
      return `Reply to ${p.to ?? "an email"}: "${truncate(p.body)}"`;
    case "block_sender":
      return `Block ${p.sender_email ?? "a sender"}`;
    case "mark_read":
      return "Mark an email as read";
    case "archive_email":
      return "Archive an email";
    case "create_event":
      return `Create event "${p.summary ?? ""}" at ${p.start ?? ""}`;
    case "delete_event":
      return "Delete a calendar event";
    case "schedule_reminder":
      return `Schedule a reminder: "${truncate(p.message)}"`;
    default:
      return `Perform: ${a.actionType}`;
  }
}

